const express = require('express');
const cors = require('cors');
const { errors } = require('celebrate');
const routes = require('./routes');

const app = express();

app.use(cors());
app.use(express.json());
app.use(routes);
app.use(errors());

//esse errors do celebrate torna as mensagens de erro de autenticação 
//  mais user friendly

module.exports = app;